public class Aluno {
    String nome;
    double nota1;
    double nota2;
    double maiornota;
    double media;
    String situacao;

    public Aluno(String nome, double nota1, double nota2, double maiornota, double media, String situacao) {
        this.nome = nome;
        this.nota1 = nota1;
        this.nota2 = nota2;
        this.maiornota = maiornota;
        this.media = media;
        this.situacao = situacao;
    }
}


